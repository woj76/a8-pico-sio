def encode_pico(ac):
	s = "\t\t"
	for col in range(8):
		bs = 0
		for row in range(8):
			if ac[row][col] == '*':
				bs = bs | (1 << row)
		s += f"0x{bs:02X},"
	s += "\n"
	return s

